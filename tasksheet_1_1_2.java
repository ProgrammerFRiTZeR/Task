public class tasksheet_1_1_2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        a += b;
        System.out.println("Addition using compound assignment: " + a);

        a -= b;
        System.out.println("Subtraction using compound assignment: " + a);

        a *= b;
        System.out.println("Multiplication using compound assignment: " + a);

        a /= b;
        System.out.println("Division using compound assignment: " + a);
    }
}          